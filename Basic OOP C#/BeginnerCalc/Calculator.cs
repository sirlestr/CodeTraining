namespace BeginnerCalc;


public class Calculator
{
    public double number1 = 0f;
    public double number2 = 0f;

    public double Addition() { return number1 + number2; }
    public double Subtract() { return number1 - number2; }
    public double Multiply() { return number1 * number2; }
    public double Divide() { return number1 / number2; }
}